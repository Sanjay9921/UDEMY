// == -> Equality of value (not type)
1==1;//true
1=='1';//true
0=='';//true
true==false;//false
0==false;//true
1==true;//true
null==undefined;//true

// === -> Triple/Strict Equality -> Value and type
1===1;//true
1==='1';//false
0===false;//false
1===true;//false

// != 
1!=2;//true
1!='1';//false

// !==
1!=='1';//true



